package org.lerot.myELH;

public class dimensionp
{
    double width;
    double height;

    public dimensionp(double length, double height)
    {
        this.width = length;
        this.height = height;
    }

    public double getWidth()
    {
        return this.width;
    }

    public double getHeight()
    {
        return this.height;
    }
}


